<script>
export default {
    data() {
        return {
            counters: {
                annualPartner: 0,
                completedProjects: 0,
                happyCustomers: 0,
                researchWork: 0,
            },
            targetCounters: {
                annualPartner: 250,
                completedProjects: 380,
                happyCustomers: 490,
                researchWork: 98,
            },
            labels: {
                annualPartner: "ANNUAL PARTNERS",
                completedProjects: "COMPLETED PROJECTS",
                happyCustomers: "HAPPY CUSTOMERS",
                researchWork: "RESEARCH WORK",
            },
            hasAnimated: false,
        };
    },
    mounted() {
        this.observeSection();
    },
    methods: {

        startCounting() {
            if (this.hasAnimated) return;
            this.hasAnimated = true;

            Object.keys(this.counters).forEach((key) => {
                this.animateCounter(key);
            });
        },

        animateCounter(counterKey) {
            const target = this.targetCounters[counterKey];
            let count = 0;
            const increment = Math.ceil(target / 100);
            const updateCounter = () => {
                if (count < target) {
                    count += increment;
                    this.counters[counterKey] = Math.min(count, target);
                    requestAnimationFrame(updateCounter);
                }
            };
            requestAnimationFrame(updateCounter);
        },

        resetCounters() {
            if (!this.hasAnimated) return;
            this.hasAnimated = false;
            Object.keys(this.counters).forEach((key) => {
                this.counters[key] = 0;
            });
        },

        observeSection() {
            const section = this.$refs.counterSection;
            const observer = new IntersectionObserver(
                (entries) => {
                    entries.forEach((entry) => {
                        if (entry.isIntersecting) {
                            this.startCounting();
                        } else {
                            this.resetCounters();
                        }
                    });
                },
                {threshold: 0.5}
            );
            observer.observe(section);
        },

    },
};
</script>

